#!/usr/bin/env node
/**
 * Copy same-origin FFmpeg core assets for Converter V1.
 * Intentionally separate from public/wasm (owned by PDF.js postinstall).
 */
import { cpSync, existsSync, mkdirSync, rmSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { createRequire } from 'node:module';

const require = createRequire(import.meta.url);
const root = path.join(path.dirname(fileURLToPath(import.meta.url)), '..');

// Resolve package root without requiring package.json export.
const coreEntry = require.resolve('@ffmpeg/core');
const coreRoot = path.resolve(path.dirname(coreEntry), '..', '..');
const destDir = path.join(root, 'public', 'converter-runtime', 'ffmpeg');

if (existsSync(destDir)) {
  rmSync(destDir, { recursive: true, force: true });
}
mkdirSync(destDir, { recursive: true });

const candidates = [
  path.join(coreRoot, 'dist', 'umd'),
  path.join(coreRoot, 'dist', 'esm'),
  path.join(path.dirname(coreEntry)),
];

let copied = false;
for (const dir of candidates) {
  const js = path.join(dir, 'ffmpeg-core.js');
  const wasm = path.join(dir, 'ffmpeg-core.wasm');
  if (existsSync(js) && existsSync(wasm)) {
    cpSync(js, path.join(destDir, 'ffmpeg-core.js'));
    cpSync(wasm, path.join(destDir, 'ffmpeg-core.wasm'));
    copied = true;
    break;
  }
}

if (!copied) {
  throw new Error('Could not locate @ffmpeg/core ffmpeg-core.js/.wasm assets');
}

writeFileSync(
  path.join(destDir, 'README.txt'),
  [
    'FFmpeg core assets for Plexity Converter.',
    'Generated by scripts/copy-converter-runtime-assets.mjs',
    'Do not place these under public/wasm — that directory is owned by PDF.js.',
    '',
    'Deployment note: these are same-origin static assets loaded lazily by',
    'src/lib/tools/converter/ffmpeg/ffmpeg-runtime.js (via toBlobURL against',
    '/converter-runtime/ffmpeg/ffmpeg-core.js and ffmpeg-core.wasm). Make sure this',
    'directory is deployed to the CDN/host alongside — and ideally slightly before —',
    'the frontend build that references it, so the first FFmpeg-only conversion never',
    '404s while the deploy is still propagating. Re-run',
    '`npm run runtime:converter` (scripts/copy-converter-runtime-assets.mjs) after',
    'bumping @ffmpeg/core to refresh these files before shipping.',
    '',
  ].join('\n'),
);

console.log('[converter-runtime] copied FFmpeg core -> public/converter-runtime/ffmpeg/');
